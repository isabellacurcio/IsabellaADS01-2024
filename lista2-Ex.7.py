#Cada pássaro consome 30 gramas de ração por dia. Escreva um programa que leia o
#número de pássaros que o criador possui e calcule a quantidade total de ração necessária por dia.
passaros=float(input('Digite a quantidade de pássaros que possui:'))

quantidaderaçao= 30

ração= passaros * quantidaderaçao

print('A quantidade necessária de ração por dia será de',ração,'gramas.')