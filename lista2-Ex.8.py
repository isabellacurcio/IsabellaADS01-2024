#Um usuário deseja converter a temperatura de Celsius para Fahrenheit. Escreva um programa que
#leia a temperatura em Celsius e exiba a temperatura equivalente em Fahrenheit.
celsius= int(float(input('Por favor informe a tempertaura atual em graus:')))

fahrenheit= ((celsius * 1.8) + 32)


print('A temperatura atual é de',fahrenheit,'Fahrenheit.')